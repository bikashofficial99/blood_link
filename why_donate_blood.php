  <div class="abouttext"><h3>About Blood Donation</h3>
  <p>
    Not all blood is the same. Blood type, also called blood group, is determined by the presence or absence of <br>
    specific antigens, which can be proteins, carbohydrates, glycoproteins, or glycolipids. Blood type is particul<br>
    arly important during blood transfusions because some blood types are incompatible with one another. If a <br>
    patient receives an incompatible blood type, their body will attack it like a life-threatening infection <br>
    rather than a life-saving transfusion.<br>
    <br>
    <br>

    Blood falls into one of four major blood groups depending upon the presence or absence of two antigens—named <br>
    A and B—on the surface of its red blood cells (and the opposite antibodies in its plasma). In addition, blood<br>
    is further classified as either positive (+) or negative (-) depending upon whether it contains a protein known<br>
    as the Rh factor. Together, the ABO system and the Rh system give us the eight main blood types:<br>
    <br>
    <br>

    A positive (A+) — Can donate cells to A+ and AB+<br>
    A negative (A-) — Can donate cells to A+/- or AB+/-<br>
    B positive (B+) — Can donate cells to B+ and AB+<br>
    B negative (B-) — Can donate cells to B+/- and AB+/-<br>
    O positive (O+) — Can donate cells to A+, B+, AB+, and O+<br>
    O negative (O-) — Can donate cells to all types (universal donor)<br>
    AB positive (AB+) — Can donate cells to AB+<br>
    AB negative (AB-) — Can donate cells to AB+/-<br>
    </p>
