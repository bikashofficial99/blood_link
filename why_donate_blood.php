    <div class="dropdown-content1">
    <p>The criteria for donor selection varies from country to country, but blood can be donated<br>
        by most people who are healthy and do not have an infection that can be transmitted through their blood.<br>
        
        The age at which people are eligible to give blood varies, but is commonly between the ages of 17 and 65.<br>
        Some countries accept donations from people from the age of 16 and extend the upper age limit beyond 65 years.<br>
        
        Healthy adults can give blood regularly – at least twice a year. Your local blood service can tell you how frequently<br>
